						<h1>SENSODRIVE SD-ERT40</h1>
<h2>Our electrical rotary table for industry</h2>
<p>Our SENSODRIVE SD-ERT40 is a precise, dynamic electrical rotary table for industry.</p>
<p>Due to its outstanding performance it is even suitable for the most demanding requirements and applications.&nbsp;The sophisticated SENSODRIVE Unireg12 controller and a brushless high-end DC motor are built into the electrical rotary table <span style="text-decoration: underline;">-</span> in a sealed housing.</p>
<p>The SD-ERT 40 enables a quick and accurate positioning – and that is without the disadvantages that&nbsp;pneumatic drives bring about. Further advantages are the high flexibility, the absolute reliability, the simple configuration&nbsp;and - not to be forgotten - the user-friendly, speedy installation.</p>
